pub fn print_topic(topic: &str) {
    match topic {
        "about" => print_about(),
        "array" => print_array(),
        "range" => print_range(),
        "assert" => print_assert(),
        "body" | "jdef" | "tdef" | "text" => print_body(),
        "check" => print_check(),
        "conditionals" => print_conditionals(),
        "loops" | "while" | "for" => print_loops(),
        "datetime" => print_datetime(),
        "delay" => print_delay(),
        "envvars" => print_envvars(),
        "expect" => print_expect(),
        "inspect" => print_inspect(),
        "json" | "json_assertions" | "jsonpath" => print_json(),
        "float" => print_float(),
        "params" | "from_cmd_param" | "cmdparam" => print_params(),
        "function" => print_function(),
        "headers" | "hdef" => print_headers(),
        "form" | "fdef" => print_form(),
        "html" => print_html(),
        "imported" => print_imported(),
        "integer" => print_integer(),
        "match" => print_match(),
        "mock" => print_mock(),
        "query_string" | "qdef" => print_query_string(),
        "request" => print_request(),
        "response" => print_response(),
        "retry" => print_retry(),
        "snapshot" => print_snapshot(),
        "string" => print_string(),
        "tuple" => print_tuple(),
        other => {
            eprintln!("unknown help topic '{other}'");
            eprintln!("Run 'def help' for a list of all topics.");
            process::exit(1);
        }
    }
}

