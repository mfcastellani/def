use std::process;

const VERSION: &str = env!("CARGO_PKG_VERSION");

// Prints `s` as data — braces in `s` are never interpreted as format tokens.
fn text(s: &str) {
    println!("{s}");
}

fn section(title: &str) {
    println!();
    text(title);
    println!();
}

